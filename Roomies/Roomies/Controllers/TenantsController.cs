using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using EFGetStarted.AspNetCore.NewDb.Models;

namespace Roomies.Controllers
{
    public class TenantsController : Controller
    {
        private readonly RoomiesContext _context;
        private long _OfficerID = 0;

        public TenantsController(RoomiesContext context)
        {
            _context = context;
        }

        // GET: Tenants
        public async Task<IActionResult> Index()
        {
            return View(await _context.Tenants.ToListAsync());
        }

        // GET: Tenants/Details/5
        public async Task<IActionResult> Details(long? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var tenant = await _context.Tenants
                .FirstOrDefaultAsync(m => m.TenantID == id);
            if (tenant == null)
            {
                return NotFound();
            }

            return View(tenant);
        }

        // GET: Tenants/Create
        public IActionResult Create()
        {
            return View();
        }

        // POST: Tenants/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("TenantID,FirstName,MiddleName,LastName,TaxIdentification,PassPortNumber,DateOfBirth,Address,ContactNumber,ContactNumber2,Note,CreatedDateTime,CreatedBy,UpdatedDateTime,UpdatedBy")] Tenant tenant)
        {
            var now = DateTime.Now;
            tenant.CreatedDateTime = now;
            tenant.CreatedBy = _OfficerID;
            tenant.UpdatedDateTime = now;
            tenant.UpdatedBy = _OfficerID;

            if (ModelState.IsValid)
            {
                _context.Add(tenant);
                await _context.SaveChangesAsync();
                return RedirectToAction(nameof(Index));
            }
            return View(tenant);
        }

        // GET: Tenants/Edit/5
        public async Task<IActionResult> Edit(long? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var tenant = await _context.Tenants.FindAsync(id);
            if (tenant == null)
            {
                return NotFound();
            }
            return View(tenant);
        }

        // POST: Tenants/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(long id, [Bind("TenantID,FirstName,MiddleName,LastName,TaxIdentification,PassPortNumber,DateOfBirth,Address,ContactNumber,ContactNumber2,Note,CreatedDateTime,CreatedBy,UpdatedDateTime,UpdatedBy")] Tenant tenant)
        {
            tenant.UpdatedDateTime = DateTime.Now;
            tenant.UpdatedBy = _OfficerID;

            if (id != tenant.TenantID)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    _context.Update(tenant);
                    await _context.SaveChangesAsync();
                }
                catch (DbUpdateConcurrencyException)
                {
                    if (!TenantExists(tenant.TenantID))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            return View(tenant);
        }
        
        private bool TenantExists(long id)
        {
            return _context.Tenants.Any(e => e.TenantID == id);
        }
    }
}
